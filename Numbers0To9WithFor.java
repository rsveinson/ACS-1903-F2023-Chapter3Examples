 /**
 * Display numbers 0 to 9 using a for
 */
public class Numbers0To9WithFor
{
    public static void main(String[] args)
    {
        System.out.println("Numbers");
        for (int count=0; count < 10; count++ )
            System.out.println(count);
        System.out.println("*******");
    }
}
